#include<stdio.h>
#include "main.h"

/**
* main - check if a character is an alphabet
*
* Return: 1 if lower and 0 if not
*/

int main(void)
{
int x;
int n;
for (x = 48; x <= 57; x++)
{
for (n = 48; n <= 57; n++)
{
if (x > 48)
{
putchar(x);
}
putchar(n);
if (x = 57 && n = 57)
{
break;
}
else
{
putchar(' ');
}
}
}

return (0);

}
